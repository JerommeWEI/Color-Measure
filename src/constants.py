def _detect_font():
    probe = tk.Tk()
    probe.withdraw()
    available = tkfont.families(probe)
    probe.destroy()
    for candidate in ("Segoe UI Variable", "Segoe UI", "Helvetica Neue"):
        if candidate in available:
            return candidate
    return "Microsoft YaHei"


